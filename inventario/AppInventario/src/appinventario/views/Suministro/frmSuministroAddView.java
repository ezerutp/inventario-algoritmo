    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            cantidad = Integer.parseInt(this.txtCantidad.getText());
        } catch (NumberFormatException e) {
            cantidad = 0;
        }
        if (validarDatos()) {
            int idpro = this.cbxProducto.getSelectedIndex() + 1;
            int idprov = this.cbxProveedor.getSelectedIndex() + 1;
            Producto pro = this.controlproducto.obtenerProducto(idpro);
            Proveedor prov = this.controlproveedor.obtenerProveedor(idprov);
            sum.setProducto(pro);
            sum.setProveedor(prov);
            sum.setCantidad(cantidad);
            Date fecha = new Date();
            sum.setFechaIngreso(fecha);
            sum.setUsuario(UserSession.getInstancia().getUser());
            boolean rs = controlador.registrarSuministro(sum);
            if (!rs) {
                JOptionPane.showMessageDialog(null, "ERROR AL REGISTRAR EL SUMINISTRO", "ERROR",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "SUMINISTRO REGISTRADO!!", "CORRECTO",
                        JOptionPane.INFORMATION_MESSAGE);
            }
            dispose();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

