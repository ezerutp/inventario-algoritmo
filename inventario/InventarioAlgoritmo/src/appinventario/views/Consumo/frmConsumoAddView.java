    private void rellenar(){
        for (ProductoStock pstock : productos) {
            this.cbxProducto.addItem(pstock.getNombre());
        }
    }

    private void setCantidadtxt(String nombre) {
        for (ProductoStock pstock : productos) {
            if (pstock.getNombre().equals(nombre)) {
                this.cantidadStock = pstock.getCantidad();
                this.txtCantidad.setText(this.cantidadStock + "");
            }
        }
    }

    private boolean validarDatos() {
        if (this.cbxProducto.getSelectedItem() == null || this.txtCantidad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (this.cantidad > this.cantidadStock || this.cantidad <= 0) {
            JOptionPane.showMessageDialog(null, "La cantidad de consumo no puede superar la cantidad en inventario.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

