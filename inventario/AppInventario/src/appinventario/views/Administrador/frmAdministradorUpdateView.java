    private boolean validarDatos() {
        if (this.txtNombre.getText().isEmpty() || this.txtApellido.getText().isEmpty() || this.txtTelefono.getText().isEmpty() || this.txtUsuario.getText().isEmpty() || this.txtCargo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

