    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        // Codigo para seleccionar un elemento de la JTable
        if (evt.getClickCount() == 2) {
            JTable target = (JTable)evt.getSource();
            int row = target.getSelectedRow();
            int id = (int) target.getValueAt(row, 0); // Es la id del elemento de la JTable.
            frmUpdateView(id); // Invocamos al metodo main modificado para que reciba el id del usuario
        }
    }//GEN-LAST:event_tablaUsuariosMouseClicked

    // Funcion para la vista update
    private void frmUpdateView(int id) {
        frmAdministradorUpdateView updateview = new frmAdministradorUpdateView(this, true, this.controlador, id);
        updateview.setVisible(true);
        actualizarTabla();
    }
    
    // Funcion para la vista nuevo
    private void frmNewView() {
        frmAdministradorAddView addview = new frmAdministradorAddView(this, true, this.controlador);
        addview.setVisible(true);
        actualizarTabla();
    }
    
    // Funcion para refrescar la tabla y visualizar los cambios
    private void actualizarTabla() {
        List<Usuario> user = controlador.obtenerTodosUsuarios();
        UsuarioTableModel modelo = new UsuarioTableModel(user);
        tablaUsuarios.setModel(modelo);
    }

