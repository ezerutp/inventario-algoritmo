    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (validarDatos()) {
            user.setNombre(this.txtNombre.getText());
            user.setApellido(this.txtApellido.getText());
            user.setTelefono(this.txtTelefono.getText());
            user.setUsuario(this.txtUsuario.getText());
            user.setCargo(this.txtCargo.getText());
            user.setAdmin(this.ckbAdmin.isSelected());
            user.setforcePass(this.ckbPass.isSelected());
            boolean rs = controlador.actualizarUsuario(id, user);
            if (!rs) {
                JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR USUARIO","ERROR",JOptionPane.ERROR_MESSAGE);
            }
            dispose();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

