    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            cantidad = Integer.parseInt(this.txtCantidad.getText());
        } catch (NumberFormatException e) {
            cantidad = 0;
        }
        if (validarDatos()) {
            Consumo consumo = new Consumo();
            String nombreProducto = "" + this.cbxProducto.getSelectedItem();
            Producto pro = this.controlproducto.obtenerProductoPorNombre(nombreProducto);
            consumo.setProducto(pro);
            consumo.setUsuario(UserSession.getInstancia().getUser());
            consumo.setCantidad(cantidad);
            Date fecha = new Date();
            consumo.setFechaSalida(fecha);
            boolean rs = controlador.registrarConsumo(consumo);
            if (!rs) {
                JOptionPane.showMessageDialog(null, "ERROR AL REGISTRAR EL CONSUMO", "ERROR",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "CONSUMO REGISTRADO!!", "CORRECTO",
                        JOptionPane.INFORMATION_MESSAGE);
            }
            dispose();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

