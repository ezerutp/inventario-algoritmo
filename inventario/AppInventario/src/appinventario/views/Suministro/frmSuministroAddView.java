    public void rellenar(){
        List<Producto> productos = this.controlproducto.obtenerTodosProductos();
        List<Proveedor> proveedores = this.controlproveedor.obtenerTodosProveedores();
        for (Producto pro : productos) {
            this.cbxProducto.addItem(pro.getNombre());
        }
        for (Proveedor prov : proveedores) {
            this.cbxProveedor.addItem(prov.getNombre());
        }
    }

    private boolean validarDatos() {
        if (this.cbxProducto.getSelectedItem() == null || this.cbxProveedor.getSelectedItem() == null || this.txtCantidad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(null, "La cantidad de suministro no puede ser menor o igual a 0.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

