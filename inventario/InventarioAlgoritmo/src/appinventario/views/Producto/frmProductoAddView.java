    private void btnGuar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuar1ActionPerformed
        if (validarDatos()) {
            producto.setNombre(this.txtNombre.getText());
            producto.setCategoria(this.txtCategoria.getText());
            producto.setDescripcion(this.txtDescripcion.getText());
            double precio= Double.parseDouble(this.txtPrecio.getText());
            producto.setPrecio(precio);
            producto.setUnidad_medida(this.txtUnidadDeMedida.getText());
            boolean rs = controlador.registrarProducto(producto);
            if (!rs) {
                JOptionPane.showMessageDialog(null, "ERROR AL REGISTRAR EL PRODUCTO","ERROR",JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "PRODUCTO REGISTRADO!!","CORRECTO",JOptionPane.INFORMATION_MESSAGE);
            }
            dispose();
        }
    }//GEN-LAST:event_btnGuar1ActionPerformed

