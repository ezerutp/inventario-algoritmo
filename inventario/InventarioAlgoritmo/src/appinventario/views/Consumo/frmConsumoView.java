     // Funcion para la vista eliminar
    private void frmEliminarView() {
        if (this.txtID.getText() != null) {
            try {
                int id = Integer.parseInt(this.txtID.getText());
                Consumo con = controlador.obtenerConsumo(id);
                if (con.getId() > 0) {
                    int opcion = JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar el suministro : [ " + con.getCantidad() + " de " + con.getProducto().getNombre() + " ] ?", "CUIDADO!", JOptionPane.YES_NO_OPTION);
                    if (opcion == JOptionPane.YES_OPTION) {
                        boolean rs = controlador.eliminarConsumo(id);
                        if (rs) {
                            JOptionPane.showMessageDialog(null, "CONSUMO ELIMINADO!!","CORRECTO",JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(null, "NO SE PUDO ELIMINAR EL CONSUMO","ERROR",JOptionPane.ERROR_MESSAGE);
                        }
                        actualizarTabla();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No existe ningun consumo con esa ID","ERROR",JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }
    // Funcion para la vista nuevo
    private void frmNewView() {
        InventarioController inv = new InventarioController();
        List<ProductoStock> productos = inv.pStock();
        if (productos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "NO EXISTE PRODUCTOS CON STOCK EN EL INVENTARIO", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            frmConsumoAddView addview = new frmConsumoAddView(this, true, this.controlador, productos);
            addview.setVisible(true);
            actualizarTabla();
        }
    }
    
    // Funcion para refrescar la tabla y visualizar los cambios
    private void actualizarTabla() {
        List<Consumo> con = controlador.obtenerTodosConsumos();
        ConsumoTableModel modelo = new ConsumoTableModel(con);
        this.tablaConsumo.setModel(modelo);
    }

